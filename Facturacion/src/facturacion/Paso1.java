    private void buscar(String valor){
       
        try{
          
        String sql= "select *from clientes where nom_Cli like '%"+valor+"%'";
        
        BaseDatos connect= new BaseDatos();
        Connection cnn= (Connection) connect.conexion();
        
        Statement st = cnn.createStatement();
        ResultSet rs= st.executeQuery(sql);
        
        while(rs.next()){
           idCli.setText(rs.getString("id_Cli"));
           apeCli.setText(rs.getString("apell_Cli"));
           direcCli.setText(rs.getString("direc_Cli"));
           telfCli.setText(rs.getString("telef_Cli"));
        }
        }catch(SQLException  e){
            JOptionPane.showMessageDialog(null,e);
        }
        
              
    }
    
