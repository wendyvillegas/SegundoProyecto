     private void buscar(String valor){
       
        try{
          
        String []titulos={"Codigo","Nombre","Marca","Precio"};
        String []fila=new String [4];    
        
        String sql= "select *from productos  where nom_Prod LIKE '%"+valor+"%'";
        modeloPro= new DefaultTableModel(null,titulos);
        BaseDatos connect= new BaseDatos();
        Connection cnn= (Connection) connect.conexion();
        
        Statement st = cnn.createStatement();
        ResultSet rs= st.executeQuery(sql);
        
        while(rs.next()){
                fila[0]= rs.getString("id_Prod");
                fila[1]= rs.getString("nom_Prod");
                fila[2]= rs.getString("marca_Prod");
                fila[3]= rs.getString("precio_Prod");
                modeloPro.addRow(fila);       
        }  
        tablaPro.setModel(modeloPro);
        }catch(SQLException  e){
            JOptionPane.showMessageDialog(null,e);
        }     
    }
    
    
