    private void registrarProductos(){
        String nombre= t_nomPro.getText();
        String id=t_idPro.getText();
        String marca= t_marcaProd.getText();
        String  precio= t_precioPro.getText();

        if(nombre.equals("")|| id.equals("")|| marca.equals("")||precio.equals("")){
            JOptionPane.showMessageDialog(this,"Ingrese los datos");           
        }else{
            Double prec= Double.parseDouble(t_precioPro.getText());
            if(prec==0) {
                JOptionPane.showMessageDialog(this,"Ingrese un valor para el producto");
            }else{
                
                    //JOptionPane.showMessageDialog(this,"Registro Exitoso");
                    BaseDatos connect= new BaseDatos();
                    Connection cn= (Connection) connect.conexion();
                    String sql="";
                    sql="INSERT INTO productos (id_Prod, nom_Prod, marca_Prod, precio_Prod) VALUES(?,?,?,?)";
                 try {
                    PreparedStatement psd= cn.prepareStatement(sql);
                    psd.setString(1, id);
                    psd.setString(2, nombre);
                    psd.setString(3, marca);
                    psd.setString(4, precio);
                    int n= psd.executeUpdate();
                    
                    if(n>0){
                        JOptionPane.showMessageDialog(null,"Registro Exitoso" );                       
                        t_nomPro.setText("");
                        t_idPro.setText("");
                        t_marcaProd.setText("");
                        t_precioPro.setText("");
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(RegistroProductos.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }
